/**
 * 四則演算
 */
public class Arithmetic {
	public static void main(String[] args) {
		int value1, value2;
		value1 = 10;
		value2 = 5;
		System.out.println("加　算=> " + (value1 + value2));
		System.out.println("減　算=> " + (value1 - value2));
		System.out.println("乗　算=> " + (value1 * value2));
		System.out.println("除　算=> " + (value1 / value2));
		System.out.println("剰余算=> " + (value1 % value2));
	}
}