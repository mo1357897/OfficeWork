/*==============================================*/
/* 九九計算                                      */
/* ・動  作 ：1の段から9の段まで繰り返し計算を実施 */
/*==============================================*/

#include <stdio.h>
 
int main(void){
 
  int i, j;
 
  /* 九九の計算 */
  for(i=1;i<10;++i) { //iの値が9になるまで繰り返し
    for(j=1;j<10;++j) {//jの値が9になるまで繰り返し
      if(j==1)
        printf("%d の段:", i);
      if(j==9)
        printf("%3d\n", i*j);
      else
        printf("%3d", i*j);
    }
  }
 
  return 0;
}
